export const searchStocks = (query) => {
    if (!query || query.length < 1) return []
    const q = query.toUpperCase()
    return STOCK_CATALOG.filter(s =>
        s.ticker.startsWith(q) ||
        s.name.toUpperCase().includes(q)
    ).slice(0, 8)
}
